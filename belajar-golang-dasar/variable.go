package main

import "fmt"

func main() {
	var name string

	name = "Vini Jumatul F"
	fmt.Println(name)

	name = "Vini Jumatul Fitri"
	fmt.Println(name)

	var nama = "vini jf"
	fmt.Println(nama)

	country := "indonesia"
	fmt.Println(country)

	country = "german"

	//multiple variabel
	var (
		firstName = "vini"
		lastName  = "jumatul"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
